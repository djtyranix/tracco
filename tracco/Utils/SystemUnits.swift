import Foundation

// system units and their multiplier based on "base"
enum SystemUnits: Double
{
    case peta   = 1e-15
    case tera   = 1e-12
    case giga   = 1e-9
    case mega   = 1e-6
    case kilo   = 1e-3
    case hecto  = 1e-2
    case deca   = 1e-1
    case base   = 1
    case deci   = 1e1
    case centi  = 1e2
    case milli  = 1e3
    case micro  = 1e6
    case nano   = 1e9
    case pico   = 1e12
    case femto  = 1e15
}
